export async function seedLobbies(): Promise<void> {
  const countResult = await db.execute(sql`
    SELECT COUNT(*) as cnt FROM special_interest_groups WHERE category = 'lobby'
  `);
  const existing = parseInt((countResult.rows[0] as any).cnt as string, 10);
  if (existing > 0) {
    log(`Lobby seed skipped — ${existing} lobby records already exist`);
    return;
  }

  log(`Seeding ${TOP_35_LOBBIES.length} top lobbying organizations...`);

  for (const lobby of TOP_35_LOBBIES) {
    await db.execute(sql`
      INSERT INTO special_interest_groups (
        id, name, acronym, tag, description, category, sentiment,
        website, headquarters, industry, data_source_name, data_source_url,
        is_active, created_at, updated_at
      )
      VALUES (
        gen_random_uuid(),
        ${lobby.name}, ${lobby.acronym}, ${lobby.tag}, ${lobby.description},
        ${lobby.category}, ${lobby.sentiment}, ${lobby.website},
        ${lobby.headquarters}, ${lobby.industry},
        ${lobby.dataSourceName}, ${lobby.dataSourceUrl},
        true, NOW(), NOW()
      )
      ON CONFLICT (tag) DO NOTHING
    `);
  }

  log(`Lobby seed complete — ${TOP_35_LOBBIES.length} organizations upserted`);
}
